export function getQuestions(boardLevel: number, isSmMode: boolean = false, customQuestions?: Record<number, { truth: string[]; dare: string[]; punishment: string[] }>, isLongDistanceMode: boolean = false) {
  if (customQuestions && Object.keys(customQuestions).length > 0) {
    const level = Math.min(boardLevel, 3);
    return customQuestions[level] || customQuestions[1] || { truth: [], dare: [], punishment: [] };
  }
  if (isLongDistanceMode) {
    const level = Math.min(boardLevel, 3);
    return LONGDISTANCE_QUESTION_BANKS[level as keyof typeof LONGDISTANCE_QUESTION_BANKS] || LONGDISTANCE_QUESTION_BANKS[1];
  }
  if (isSmMode) {
    const smLevel = Math.min(boardLevel, 3);
    return SM_QUESTION_BANKS[smLevel as keyof typeof SM_QUESTION_BANKS] || SM_QUESTION_BANKS[1];
  }
  const level = Math.min(boardLevel, 3);
  return QUESTION_BANKS[level as keyof typeof QUESTION_BANKS] || QUESTION_BANKS[1];
}